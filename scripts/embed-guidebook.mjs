// Embed the user guidebook into the app bundle.
//
// Reads the self-contained guidebook (docs/guidebook/hr-analytics-guidebook.html)
// and writes it, as a JS string constant, into src/help/guidebookHtml.ts so the
// single-file build ships the guide inline — rendered in the in-app Guide view via
// an <iframe srcdoc> with no network fetch. Mirrors scripts/embed-demo.mjs.
//
// The guidebook contains no <script>; even so we escape </script> and </style> as a
// belt-and-suspenders measure (the final build base64-encodes the whole bundle, and
// srcdoc attribute-encodes the value, so neither could break the host HTML — but the
// escape keeps the generated .ts source robust under any inlining path).
//
// Regenerate after editing the guidebook:  npm run embed-guidebook

import * as fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const SRC = path.join(root, "docs", "guidebook", "hr-analytics-guidebook.html");
const OUT = path.join(root, "src", "help", "guidebookHtml.ts");

const html = fs.readFileSync(SRC, "utf8");
const literal = JSON.stringify(html).replace(/<\/(script|style)/gi, "<\\/$1");

const banner = `// AUTO-GENERATED by scripts/embed-guidebook.mjs — do not edit by hand.
// Source: docs/guidebook/hr-analytics-guidebook.html (${html.length.toLocaleString("en-US")} chars).
// This is the self-contained user guide the app renders in the in-app Guide view
// (<iframe srcdoc>). Regenerate after editing the guidebook with: npm run embed-guidebook
`;

fs.mkdirSync(path.dirname(OUT), { recursive: true });
fs.writeFileSync(OUT, `${banner}export const GUIDEBOOK_HTML = ${literal};\n`);

console.log(`embedded guidebook → src/help/guidebookHtml.ts (${html.length} chars)`);
